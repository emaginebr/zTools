using Microsoft.Extensions.Options;
using Moq;
using Moq.Protected;
using Newtonsoft.Json;
using zTools.Domain.Services;
using zTools.DTO.InVideo;
using zTools.DTO.Settings;
using System.Net;
using System.Net.Http;
using System.Threading;
using System.Threading.Tasks;
using Xunit;

namespace zTools.Tests.Domain.Services
{
    public class InVideoServiceTests
    {
        private readonly Mock<IOptions<InVideoSetting>> _mockInVideoSettings;
        private readonly InVideoSetting _inVideoSetting;
        private readonly HttpClient _httpClient;

        public InVideoServiceTests()
        {
            _inVideoSetting = new InVideoSetting
            {
                ApiUrl = "https://pro-api.invideo.io/v1/generate-video",
                ApiKey = "test-api-key-12345"
            };

            _mockInVideoSettings = new Mock<IOptions<InVideoSetting>>();
            _mockInVideoSettings.Setup(x => x.Value).Returns(_inVideoSetting);

            _httpClient = new HttpClient();
        }

        private static InVideoResponse CreateTestResponse()
        {
            return new InVideoResponse
            {
                VideoUrl = "https://invideo.io/videos/test-video-123",
                Title = "Test Video",
                Description = "A test video generated by InVideo AI"
            };
        }

        [Fact]
        public void Constructor_WithValidSettings_InitializesSuccessfully()
        {
            var service = new InVideoService(_httpClient, _mockInVideoSettings.Object);
            Assert.NotNull(service);
        }

        [Fact]
        public void Constructor_WithNullHttpClient_ThrowsArgumentNullException()
        {
            Assert.Throws<System.ArgumentNullException>(() => new InVideoService(null, _mockInVideoSettings.Object));
        }

        [Fact]
        public void Constructor_WithNullSettings_ThrowsArgumentNullException()
        {
            Assert.Throws<System.ArgumentNullException>(() => new InVideoService(_httpClient, null));
        }

        [Fact]
        public async Task GenerateVideoAsync_WithPrompt_ReturnsResponse()
        {
            // Arrange
            var mockHandler = new Mock<HttpMessageHandler>();
            var expectedResponse = CreateTestResponse();
            var responseJson = JsonConvert.SerializeObject(expectedResponse);

            mockHandler.Protected()
                .Setup<Task<HttpResponseMessage>>(
                    "SendAsync",
                    ItExpr.IsAny<HttpRequestMessage>(),
                    ItExpr.IsAny<CancellationToken>())
                .ReturnsAsync(new HttpResponseMessage
                {
                    StatusCode = HttpStatusCode.OK,
                    Content = new StringContent(responseJson)
                });

            var httpClient = new HttpClient(mockHandler.Object);
            var service = new InVideoService(httpClient, _mockInVideoSettings.Object);

            // Act
            var result = await service.GenerateVideoAsync("Create a video about cats");

            // Assert
            Assert.NotNull(result);
            Assert.Equal(expectedResponse.VideoUrl, result.VideoUrl);
            Assert.Equal(expectedResponse.Title, result.Title);
            Assert.Equal(expectedResponse.Description, result.Description);
        }

