        [Fact]
        public async Task GenerateVideoAsync_WhenApiReturnsErrorWithMessage_ThrowsInvalidOperationException()
        {
            // Arrange
            var mockHandler = new Mock<HttpMessageHandler>();
            var errorResponse = new InVideoErrorResponse
            {
                Error = new InVideoError
                {
                    Message = "Invalid API key",
                    Code = "invalid_api_key"
                }
            };
            var errorJson = JsonConvert.SerializeObject(errorResponse);

            mockHandler.Protected()
                .Setup<Task<HttpResponseMessage>>(
                    "SendAsync",
                    ItExpr.IsAny<HttpRequestMessage>(),
                    ItExpr.IsAny<CancellationToken>())
                .ReturnsAsync(new HttpResponseMessage
                {
                    StatusCode = HttpStatusCode.Unauthorized,
                    Content = new StringContent(errorJson)
                });

            var httpClient = new HttpClient(mockHandler.Object);
            var service = new InVideoService(httpClient, _mockInVideoSettings.Object);

            // Act & Assert
            var exception = await Assert.ThrowsAsync<System.InvalidOperationException>(
                async () => await service.GenerateVideoAsync("test"));

            Assert.Equal("Invalid API key", exception.Message);
        }

