        [Fact]
        public void InVideoSetting_ConfigurationValues_AreAccessible()
        {
            Assert.Equal("https://pro-api.invideo.io/v1/generate-video", _inVideoSetting.ApiUrl);
            Assert.Equal("test-api-key-12345", _inVideoSetting.ApiKey);
        }

        [Fact]
        public void InVideoRequest_Serialization_ProducesCorrectJson()
        {
            // Arrange
            var request = new InVideoRequest
            {
                Prompt = "Test video",
                Duration = 10.0,
                Platform = "tiktok",
                Voice = "female",
                Accent = "british"
            };

            // Act
            var json = JsonConvert.SerializeObject(request);
            var deserialized = JsonConvert.DeserializeObject<InVideoRequest>(json);

            // Assert
            Assert.NotNull(deserialized);
            Assert.Equal(request.Prompt, deserialized.Prompt);
            Assert.Equal(request.Duration, deserialized.Duration);
            Assert.Equal(request.Platform, deserialized.Platform);
            Assert.Equal(request.Voice, deserialized.Voice);
            Assert.Equal(request.Accent, deserialized.Accent);
        }

        [Fact]
        public void InVideoResponse_Deserialization_ParsesCorrectly()
        {
            // Arrange
            var responseJson = @"{
                ""video_url"": ""https://invideo.io/videos/abc123"",
                ""title"": ""My Video"",
                ""description"": ""A great video""
            }";

            // Act
            var response = JsonConvert.DeserializeObject<InVideoResponse>(responseJson);

            // Assert
            Assert.NotNull(response);
            Assert.Equal("https://invideo.io/videos/abc123", response.VideoUrl);
            Assert.Equal("My Video", response.Title);
            Assert.Equal("A great video", response.Description);
        }

        [Fact]
        public void InVideoErrorResponse_Deserialization_ParsesCorrectly()
        {
            // Arrange
            var errorJson = @"{
                ""error"": {
                    ""message"": ""Rate limit exceeded"",
                    ""code"": ""rate_limit""
                }
            }";

            // Act
            var errorResponse = JsonConvert.DeserializeObject<InVideoErrorResponse>(errorJson);

            // Assert
            Assert.NotNull(errorResponse);
            Assert.NotNull(errorResponse.Error);
            Assert.Equal("Rate limit exceeded", errorResponse.Error.Message);
            Assert.Equal("rate_limit", errorResponse.Error.Code);
        }
